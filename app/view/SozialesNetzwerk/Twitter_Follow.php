<!-- Von Twitter generierter Code, der dem Kunden die Möglichkeit bietet, unserer Twitter-Seite durch einen eingebundenen Button zu folgen -->
<script>
    !function (d, s, id) {
        var js, fjs = d.getElementsByTagName(s)[0], p = /^http:/.test(d.location) ? 'http' : 'https';
        if (!d.getElementById(id)) {
            js = d.createElement(s);
            js.id = id;
            js.src = p + '://platform.twitter.com/widgets.js';
            fjs.parentNode.insertBefore(js, fjs);
        }
    }(document, 'script', 'twitter-wjs');
</script>
    <!-- Aufruf des Plugins an gewünschter Stelle -->
    <a href="https://twitter.com/FashFact" class="twitter-follow-button" data-show-count="false">Follow @FashFact</a>
    
